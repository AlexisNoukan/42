#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	d;

	if (nb < 0)
	{
		write(1, "-", 1);
		nb = -nb;
	}
	if (nb >= 10)
	{
		ft_putnbr(nb / 10);
	}
	d = '0' + (nb % 100);
	write(1, &d, 1);
}
